
public class Attack {
	
	String attackName;
	String attackType;
	int pwr;
	int pp;
	int acc;
	
	
	/**
	 * Set Attackname
	 */
	void setAttackName(String attack) {
		this.attackName = attack;
	}
	
	/**
	 * Get Attackname
	 * @return String
	 */
	String getAttackName() {
		return attackName;
	}
	
	/**
	 * Set Attacktype
	 */
	void setAttackType(String type) {
		this.attackType = type;
	}
	
	/**
	 * Get Attacktype
	 * @return String
	 */
	String getAttackType() {
		return attackType;
	}
	
	
}
